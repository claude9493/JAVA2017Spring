package exercise5;

public class Student {
	private String studentNo,  name;
	private int grade;
	
	public String get_No(){
		return this.studentNo;
	}
	public void set_No(String No){
		this.studentNo = No;
	}
	public String get_name(){
		return this.name;
	}
	public void set_name(String name){
		this.name = name;
	}
	public int get_grade(){
		return grade;
	}
	public void set_grade(int grade){
		this.grade = grade;
	}
	

}
