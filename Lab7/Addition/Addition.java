package Addition;

public class Addition {

	public int sum(int addend, int augend) {
		return addend + augend;
	}

	public double sum(double addend, double augend) {
		return addend + augend;
	}

	public long sum(long addend, long augend) {
		return addend + augend;
	}

	public float sum(float addend, float augend) {
		return addend + augend;
	}

	public String sum(String addend, String augend) {
		return addend + augend;
	}
}
